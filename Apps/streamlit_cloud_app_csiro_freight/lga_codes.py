# ---------------------------------------------------------------------------
# State mapping — derived from the first digit of the LGA numeric code
#   1=NSW  2=VIC  3=QLD  4=SA  5=WA  6=TAS  7=NT  8=ACT
# ---------------------------------------------------------------------------
_STATE_PREFIX: dict[str, str] = {
    "1": "NSW", "2": "VIC", "3": "QLD", "4": "SA",
    "5": "WA",  "6": "TAS", "7": "NT",  "8": "ACT",
}

def _state(code: str) -> str:
    return _STATE_PREFIX.get(code.replace("LGA_", "")[0], "Other")

# code -> state abbreviation
LGA_STATE: dict[str, str] = {code: _state(code) for code in LGA_CODES}

# state abbreviation -> sorted list of LGA names
STATE_LGAS: dict[str, list[str]] = {}
for _code, _state_abbr in LGA_STATE.items():
    STATE_LGAS.setdefault(_state_abbr, []).append(LGA_CODES[_code])
STATE_LGAS = {s: sorted(names) for s, names in sorted(STATE_LGAS.items())}

